package day_9;

public class C3ForLoop {
    public static void main(String[] args) {
        //ITERATION (Repetition) i.e. doing something again and again in a loop
        //to repeat something means to iterate

        for(int counter = 1; counter<=33; counter++ ){
            System.out.println(counter + "Subhanallah");

        }
        int n=50;
        int number ;
        for( number=1; number<=n; number++) {
            System.out.println(number);

        }
        System.out.println(number);

    }

}
