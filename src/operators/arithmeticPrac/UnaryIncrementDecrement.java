package operators.arithmeticPrac;

public class UnaryIncrementDecrement {
    public static void main(String[]amk){
        int i = 5 ;
        System.out.println(i);
        i++;
        System.out.println(i);
        ++i;
        System.out.println(i);
        i--;
        System.out.println(i);
        --i;
        System.out.println(i);
    }
}
