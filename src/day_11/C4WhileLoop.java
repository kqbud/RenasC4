package day_11;

public class C4WhileLoop {
    public static void main(String[]yay){
        //Using FOR loop
        for(int i = 1; i<=5; i++){
            System.out.println(i);
        }
        System.out.println();
        //Using while loop
        int j = 1;
        while(j<=5){
            System.out.println(j);
            j++;
        }
    }
}
