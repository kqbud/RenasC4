package day_5;

public class C5UnaryDemo {
    public static void main(String[] args) {
        int i =5 ;
        System.out.println(i);
        i++;
        System.out.println(i);
        ++i;
        System.out.println(i);
        System.out.println(++i); //"pre-increment" operator
        System.out.println(i++); // increment operator
        System.out.println(i);
        //++ soldaysa once arttir sonra variablei uygula eger sagdaysa once variablei koy sonra arttir demek
    }
}
